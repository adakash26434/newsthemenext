    // ── AD → BS conversion ────────────────────────────────
    /**
     * Convert a Gregorian date (DateTime or date string) to BS.
     * Returns ['year'=>int,'month'=>int,'day'=>int,'month_name_np'=>str,'month_name_en'=>str]
     * or null if out of supported range.
     */
    public static function fromAD(string|\DateTimeInterface $date): ?array
    {
        if (is_string($date)) {
            $dt = new \DateTime($date);
        } else {
            $dt = clone $date;
        }
        $dt->setTime(0, 0, 0);

        // Reference: 1 Baisakh 2000 BS = 13 April 1943 AD
        $refAD = new \DateTime('1943-04-13');
        $refAD->setTime(0, 0, 0);

        if ($dt < $refAD) return null;

        $totalDays = (int)$dt->diff($refAD)->days;

        $bsYear  = 2000;
        $bsMonth = 1;
        $bsDay   = 1;

        foreach (self::$cal as $year => $months) {
            foreach ($months as $mi => $daysInMonth) {
                if ($totalDays < $daysInMonth) {
                    $bsYear  = $year;
                    $bsMonth = $mi + 1;
                    $bsDay   = $totalDays + 1;
                    return [
                        'year'         => $bsYear,
                        'month'        => $bsMonth,
                        'day'          => $bsDay,
                        'month_name_np'=> self::MONTHS_NP[$bsMonth],
                        'month_name_en'=> self::MONTHS_EN[$bsMonth],
                        'dow_np'       => self::DAYS_NP[(int)$dt->format('w')],
                    ];
                }
                $totalDays -= $daysInMonth;
            }
        }
        return null; // out of range
    }

    // ── Formatted string helpers ───────────────────────────

    /** "२०८२ बैशाख १५ गते, आइतबार" */
    public static function formatFull(string $adDate): string
    {
        $bs = self::fromAD($adDate);
        if (!$bs) return '';
        return np_number($bs['year']) . ' ' . $bs['month_name_np'] . ' '
             . np_number($bs['day']) . ' गते, ' . $bs['dow_np'];
    }

    /** "२०८२ बैशाख १५" */
    public static function formatShort(string $adDate): string
    {
        $bs = self::fromAD($adDate);
        if (!$bs) return '';
        return np_number($bs['year']) . ' ' . $bs['month_name_np'] . ' ' . np_number($bs['day']);
    }

    /** "2082-01-15" (BS ISO-like) */
    public static function formatISO(string $adDate): string
    {
        $bs = self::fromAD($adDate);
        if (!$bs) return '';
        return sprintf('%04d-%02d-%02d', $bs['year'], $bs['month'], $bs['day']);
    }

    /** Today's BS date: "२०८२ बैशाख १५ गते, आइतबार" */
    public static function today(): string
    {
        return self::formatFull(date('Y-m-d'));
    }

    /** Total days in a BS year */
    public static function daysInYear(int $bsYear): int
    {
        if (!isset(self::$cal[$bsYear])) return 365;
        return array_sum(self::$cal[$bsYear]);
    }

    /** Days in a BS month */
    public static function daysInMonth(int $bsYear, int $bsMonth): int
    {
        return self::$cal[$bsYear][$bsMonth - 1] ?? 30;
    }
