$ignore = db_driver() === 'mysql' ? 'INSERT IGNORE' : 'INSERT OR IGNORE';
$added  = 0;
$now    = new DateTime();

foreach ($articles as $idx => $art) {
    // Find category ID
    $cat_row = $db->prepare("SELECT id FROM categories WHERE slug=?");
    $cat_row->execute([$art['cat']]);
    $cat_id = (int)($cat_row->fetchColumn());
    if (!$cat_id) { echo "Skip: cat not found: {$art['cat']}\n"; continue; }

    // Find author ID
    $au_row = $db->prepare("SELECT id FROM authors WHERE slug=?");
    $au_row->execute([$art['author']]);
    $author_id = (int)($au_row->fetchColumn());
    if (!$author_id) $author_id = 1; // fallback to team

    // Generate slug
    $slug_base = slug_from_title($art['title']);
    $slug = $slug_base;
    $si = 1;
    while (true) {
        $existing_slug = $db->prepare("SELECT id FROM articles WHERE slug=?");
        $existing_slug->execute([$slug]);
        if (!$existing_slug->fetchColumn()) break;
        $slug = $slug_base . '-' . $si++;
    }

    // Published at: spread over last 30 days
    $pub_date = clone $now;
    $pub_date->modify('-' . ($idx * 22 + rand(0, 20)) . ' hours');

    $db->prepare(
        "INSERT INTO articles (title, slug, content, summary, status, featured, is_breaking,
         image_url, category_id, author_id, published_at, views, trending_score, type)
         VALUES (?, ?, ?, ?, 'published', ?, ?, ?, ?, ?, ?, ?, ?, 'news')"
    )->execute([
        $art['title'],
        $slug,
        $art['content'],
        $art['summary'],
        ($art['featured'] ?? 0) ? 1 : 0,
        ($art['is_breaking'] ?? 0) ? 1 : 0,
        $art['img'],
        $cat_id,
        $author_id,
        $pub_date->format('Y-m-d H:i:s'),
        rand(50, 3500),     // random views
        rand(5, 200),       // trending score
    ]);
    $added++;
    echo "✓ Added: {$art['title']}\n";
}

echo "\n🎉 Added {$added} articles total!\n";
